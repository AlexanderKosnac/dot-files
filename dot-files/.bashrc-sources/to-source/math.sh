#!/bin/bash

bytes-to-human() {
    b=${1:-0}; d=''; s=0; S=(Bytes {K,M,G,T,P,E,Z,Y}iB)
    while ((b > 1024)); do
        d="$(printf ".%02d" $((b % 1024 * 100 / 1024)))"
        b=$((b / 1024))
        let s++
    done
    echo "$b$d ${S[$s]}"
}

get-percentage() {
    $python_3_8 -c "print(\"{0:.0%}\".format($1/$2))"
}

